    BATCH_SIZE = 20
    MAX_WORKERS = 10
    success_count = 0
    fail_count = 0
    
    for i in range(0, len(pending_ids), BATCH_SIZE):
        batch = pending_ids[i:i+BATCH_SIZE]
        batch_num = i // BATCH_SIZE + 1
